CREATE TABLE administrador (
	id INT  PRIMARY KEY NOT NULL AUTO_INCREMENT,
	login VARCHAR (50) NOT NULL,
	senha VARCHAR(25) NOT NULL);


CREATE TABLE produtos(
	produto_id INT  PRIMARY KEY NOT NULL AUTO_INCREMENT,
	tipo VARCHAR (100) NOT NULL,
	imagem VARCHAR (200) NOT NULL,
	nome VARCHAR (200) NOT NULL,
	descricao VARCHAR (200) NOT NULL);


	
CREATE TABLE tb_pedidos(
	codigo INT  PRIMARY KEY NOT NULL AUTO_INCREMENT,
	datahora TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	cliente VARCHAR (200),
	celular VARCHAR (20),
	email VARCHAR (50));

CREATE TABLE tb_pedido_itens(
	codigo INT  PRIMARY KEY NOT NULL AUTO_INCREMENT,
	produto VARCHAR (200) NOT NULL,
	tipo VARCHAR (100) NOT NULL,
	descricao VARCHAR (200) NOT NULL,
	qtd INT  NOT NULL,
    codproduto INT NOT NULL,
	pedido INT  NOT NULL,
    constraint fk_pedido_itens_produtos foreign key(codproduto) references produtos(produto_id),
    constraint fk_pedido_itens_pedidos foreign key(pedido) references tb_pedidos(codigo)
    );
	
	



